package com.cakefactory.storefront;

public class Pastry {

	private String name;
	private double price;
	
	public String getName() {
		return name;
	}
	
	public void setName(String value) {
		name = value;
	}
	
	public double getPrice() {
		return price;
	}
	
	public void setPrice(double value) {
		price = value;
	}
	
}
